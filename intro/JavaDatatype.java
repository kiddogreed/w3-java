package intro;

public class JavaDatatype {
   int myNum = 9;
   float myFloatNum = 8.99f;
   char myLetter = 'A';
   boolean myBool = false;
   String myText = "Hello world 3000";


   //typecasting from small to large

   int myInt = 9;
   double myDouble = myInt; // Automatic casting: int to double //increased type

   double myDoubles = 9.78d;
   int myInts = (int) myDoubles; // Manual casting: double to int
}
